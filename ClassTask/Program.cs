string word = "test";

string Replace(string word, char oldLetter, char newLetter)
{
	string newWord = "";
	for (int i = 0; i < word.Length; i++)
	{
		if (word[i] == oldLetter)
		{
			newWord += newLetter;
		}
		else
		{
			newWord += word[i];
		}
	}
	return newWord;
}
